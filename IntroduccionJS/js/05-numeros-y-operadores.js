console.log('El resultado de la resta de ' + numero1 + ' y ' + numero2 + ' es: ' + (numero2 - numero1)) // Imprime el resultado de la resta de los números
console.log(`El resultado de la resta de ${numero1} y ${numero2} es: ${numero2 - numero1}`); // Imprime el resultado de la resta de los números utilizando template literals

