// Booleans
const esVerdadero = true; // Variable booleana con valor verdadero
const esFalso = false; // Variable booleana con valor falso

const boolean3 = 'true'; // Variable con valor de cadena, no es un booleano

console.log(esVerdadero); // Imprime el valor de la variable esVerdadero
console.log(typeof esVerdadero); // Imprime el tipo de dato de la variable esVerdadero
console.log(esFalso); // Imprime el valor de la variable esFalso
console.log(typeof esFalso); // Imprime el tipo de dato de la variable esFalso

console.log(boolean3); // Imprime el valor de la variable boolean3
console.log(typeof boolean3); // Imprime el tipo de dato de la variable boolean3

// Booleans por medio del constructor Boolean
const boolean4 = new Boolean(true); // Crea un objeto Boolean con valor verdadero
console.log(boolean4); // Imprime el objeto Boolean creado con el constructor
console.log(typeof boolean4); // Imprime el tipo de dato del objeto Boolean creado con el constructor